package com.jiuwei.entity;

public class Openid {
    private double id;
    private String openId;
    private String name;
    private String tacitly;
    private String srkl;
    private String dscs;
    private double state;

    public double getId() {
        return id;
    }

    public void setId(double id) {
        this.id = id;
    }

    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTacitly() {
        return tacitly;
    }

    public void setTacitly(String tacitly) {
        this.tacitly = tacitly;
    }

    public String getSrkl() {
        return srkl;
    }

    public void setSrkl(String srkl) {
        this.srkl = srkl;
    }

    public String getDscs() {
        return dscs;
    }

    public void setDscs(String dscs) {
        this.dscs = dscs;
    }

    public double getState() {
        return state;
    }

    public void setState(double state) {
        this.state = state;
    }
}
